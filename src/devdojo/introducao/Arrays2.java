package devdojo.introducao;

public class Arrays2 {
    public static void main(String[] args) {
        String[] nomes = new String[3];
        nomes[0] = "Luffy";
        nomes[1] = "Goku";
        nomes[2] = "Kurogane";

        for (int i = 0; i < nomes.length; i++) {
            System.out.println(nomes[i]);
        }
    }
}
